function add(a: number, b: number) {
  let result = `The sum of two numbers is ${a + b}`;
  console.log(result);
}

function sub(a: number, b: number) {
  let result = `The sum of two numbers is ${a - b}`;
  console.log(result);
}

function mul(a: number, b: number) {
  let result = `The sum of two numbers is ${a * b}`;
  console.log(result);
}

function div(a: number, b: number) {
  let result = `The sum of two numbers is ${a / b}`;
  console.log(result);
}

add(3, 5);
sub(10, 5);
mul(3, 5);
div(15, 5);

